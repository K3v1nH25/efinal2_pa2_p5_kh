package com.example.demo.repository.modelo.dto;

public class MatriculaDTO {
	private String cedulaEstudiante;
	private String codigoMateriaUno;
	private String codigoMateriaDos;
	private String codigoMateriaTres;
	private String codigoMateriaCuatro;

	public String getCedulaEstudiante() {
		return cedulaEstudiante;
	}

	public void setCedulaEstudiante(String cedulaEstudiante) {
		this.cedulaEstudiante = cedulaEstudiante;
	}

	public String getCodigoMateriaUno() {
		return codigoMateriaUno;
	}

	public void setCodigoMateriaUno(String codigoMateriaUno) {
		this.codigoMateriaUno = codigoMateriaUno;
	}

	public String getCodigoMateriaDos() {
		return codigoMateriaDos;
	}

	public void setCodigoMateriaDos(String codigoMateriaDos) {
		this.codigoMateriaDos = codigoMateriaDos;
	}

	public String getCodigoMateriaTres() {
		return codigoMateriaTres;
	}

	public void setCodigoMateriaTres(String codigoMateriaTres) {
		this.codigoMateriaTres = codigoMateriaTres;
	}

	public String getCodigoMateriaCuatro() {
		return codigoMateriaCuatro;
	}

	public void setCodigoMateriaCuatro(String codigoMateriaCuatro) {
		this.codigoMateriaCuatro = codigoMateriaCuatro;
	}

}
